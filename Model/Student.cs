public class Student{
	public Id {get; set;}
	public Fullname {get; set;}
	public Address {get; set;}
	public PhoneNo {get; set;}
	
	public Student(){}
	
	public Student(Id, Fullname,Address,PhoneNo){
		this.Id = Id;
		this.Fullname = Fullname;
		this.Address = Address;
		this.PhoneNo = PhoneNo;
	}
}